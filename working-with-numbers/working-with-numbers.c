#include <stdio.h>
#include <stdlib.h>
#include <math.h>


int main() {

    printf("%f \n", 500.465 + 1);
    printf("%f \n", pow(2, 3) ); // pow = (param1, param2) param1 to the power of param2
    printf("%f \n", ceil(47.455) ); // ceil = round up
    printf("%f \n", floor(47.455) ); // floor = round down

    return 0;
}